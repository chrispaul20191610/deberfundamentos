    private void btnsigfacturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsigfacturaActionPerformed

        DefaultTableModel modelo = (DefaultTableModel) tablafactura.getModel();
        modelo.setRowCount(0);
        String[] c = new String[7];
        try {
            _factura.siguiente();
            _cliente.siguiente();
            _vendedor.siguiente();
            lblclavefactura.setText(_factura._ventas().getString("claveFactura"));
            lblclavecliente.setText(_factura._ventas().getString("claveCliente"));
            lblclavevend.setText(_factura._ventas().getString("claveVendedor"));
            lblfechafac.setText(_factura._ventas().getString("fechaFactura"));
            lblnomclien.setText(_cliente._ventas().getString("nomCliente"));
            lblnomvend.setText(_vendedor._ventas().getString("nomVendedor"));

            _delfactura.siguiente();
            int claveparafactura = _delfactura._ventas().getInt("claveProducto");
            int cantidad = _delfactura._ventas().getInt("cantProducto");
            _producto.vercompra(claveparafactura);
            for (int i = 0; i < cantidad; i++) {

                _producto.siguiente();
                c[0] = String.valueOf(_producto._ventas().getInt("claveProducto"));
                c[1] = _producto._ventas().getString("nomProducto");
                c[2] = _producto._ventas().getString("precioProducto");
                c[3] = String.valueOf(_producto._ventas().getInt("cantProducto"));
                c[4] = _producto._ventas().getString("unidProducto");
                modelo.addRow(c);

            }

            tablafactura.setModel(modelo);

        } catch (SQLException ex) {
            Logger.getLogger(WinPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnsigfacturaActionPerformed

